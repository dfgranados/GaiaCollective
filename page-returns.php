<h4>Returns</h4>
Our policy lasts 14 days. If 14 days have gone by since your purchase, unfortunately we can't offer you a refund or exchange.
Please send us an e-mail to gaiacollective@gmail.com stating why you would like a refund. Since many of our items are made custom, we only accept returns for items that are clearly damaged.
To be eligible for a return, your item must be unused and in the same condition that you received it.
Several types of goods are exempt from being returned including products that are intimate or sanitary goods, hazardous materials, or flammable liquids or gases. We also cannot accept returns of health and personal care items.

<h4> Refunds </h4>
Once your return is received and inspected, we will send you an email to notify you that we have received your returned item. We will also notify you of the approval or rejection of your refund.
If you are approved, then your refund will be processed.

<h4> Late or missing refunds</h4>
If you haven't received a refund yet, first check your bank account again.
Then contact your credit card company, it may take some time before your refund is officially posted.
Next contact your bank. There is often some processing time before a refund is posted.
If you've done all of this and you still have not received your refund yet, please contact us at support@shopify.com.

<h4> Exchanges (if applicable)</h4>
We only replace items if they are defective or damaged.  If you need to exchange it for the same item, send us an email at 
gaiacollective@gmail.com 

<h4> Shipping</h4>
Once we determine the reason for the return, we will provide you with an address to ship your item. 
You will be responsible for paying for your own shipping costs for returning your item. Shipping costs are non-refundable. If you receive a refund, the cost of return shipping will be deducted from your refund.
Depending on where you live, the time it may take for your exchanged product to reach you, may vary.
If you are shipping an item over $75, you should consider using a trackable shipping service or purchasing shipping insurance. We don't guarantee that we will receive your returned item.
